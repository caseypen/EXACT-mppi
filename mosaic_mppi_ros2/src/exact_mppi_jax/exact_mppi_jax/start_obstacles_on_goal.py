"""Relay node: start dynamic obstacles when a navigation goal is received.

The Gazebo dynamic-obstacle driver (gazebo_dynamic_obstacles) is generated by
corridor_pedestrian_scene with ``start_paused: true`` and waits for an
``std_msgs/Empty`` on ``/start_dynamic_obstacles`` before pedestrians move.

In the original mosaic_mppi_jax workspace the global_ref_path node published
that trigger when a goal was received. EXACT's global_ref_path bridge does not
implement that behavior, so this small standalone relay reproduces the same
semantics without modifying the bridge: on the first PoseStamped seen on
``/goal_pose`` it publishes the Empty trigger (a few times for reliable
delivery), then stays idle.

Works for both auto_goal:=true and manual RViz "2D Goal Pose".
"""

import rclpy
from rclpy.node import Node
from rclpy.qos import QoSProfile, QoSDurabilityPolicy
from geometry_msgs.msg import PoseStamped
from std_msgs.msg import Empty


class StartObstaclesOnGoal(Node):
    def __init__(self) -> None:
        super().__init__("start_obstacles_on_goal")

        self.declare_parameter("goal_topic", "/goal_pose")
        self.declare_parameter("start_topic", "/start_dynamic_obstacles")
        self.declare_parameter("repeat", 5)
        self.declare_parameter("repeat_period", 0.2)

        goal_topic = self.get_parameter("goal_topic").get_parameter_value().string_value
        self.start_topic = self.get_parameter("start_topic").get_parameter_value().string_value
        self.repeat = int(self.get_parameter("repeat").get_parameter_value().integer_value)
        self.repeat_period = float(
            self.get_parameter("repeat_period").get_parameter_value().double_value
        )

        # Latched so a late-joining gazebo_dynamic_obstacles still gets it.
        latched = QoSProfile(depth=1)
        latched.durability = QoSDurabilityPolicy.TRANSIENT_LOCAL
        self._start_pub = self.create_publisher(Empty, self.start_topic, latched)
        self._goal_sub = self.create_subscription(
            PoseStamped, goal_topic, self._goal_cb, 10
        )

        self._triggered = False
        self._sent = 0
        self._timer = None
        self.get_logger().info(
            f"Waiting for first goal on {goal_topic}; will trigger {self.start_topic}"
        )

    def _goal_cb(self, _msg: PoseStamped) -> None:
        if self._triggered:
            return
        self._triggered = True
        self.get_logger().info(
            f"Goal received -> starting dynamic obstacles via {self.start_topic}"
        )
        self._send_once()
        if self.repeat > 1:
            self._timer = self.create_timer(self.repeat_period, self._send_once)

    def _send_once(self) -> None:
        self._start_pub.publish(Empty())
        self._sent += 1
        if self._sent >= self.repeat and self._timer is not None:
            self._timer.cancel()
            self._timer = None


def main(args=None) -> None:
    rclpy.init(args=args)
    node = StartObstaclesOnGoal()
    try:
        rclpy.spin(node)
    except KeyboardInterrupt:
        pass
    finally:
        node.destroy_node()
        if rclpy.ok():
            rclpy.shutdown()


if __name__ == "__main__":
    main()
